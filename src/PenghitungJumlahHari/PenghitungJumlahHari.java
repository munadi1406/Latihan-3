    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(TFTahun.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Error : Isian Tahun Masih Kosong","Error : Isian Tahun Masih Kosong", JOptionPane.WARNING_MESSAGE);
        }else{
            int tahun = Integer.parseInt(TFTahun.getText());
            int jumlahHari;
            if(CBBulan.getSelectedItem().equals("Februari")){
                if(((tahun % 4 == 0) && !(tahun % 100 == 0) || (tahun % 400 ==0)))jumlahHari = 29;
                else jumlahHari = 28;
            }else if(CBBulan.getSelectedItem().equals("april") || CBBulan.getSelectedItem().equals("juni")|| CBBulan.getSelectedItem().equals("September") || CBBulan.getSelectedItem().equals("November")){
                jumlahHari = 30;
            }else{
                jumlahHari = 31;
            }
            LHasil.setText("Jumlah Hari Pada Bulan " + CBBulan.getSelectedItem() + " Tahun " + tahun + " adalah " + jumlahHari);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

